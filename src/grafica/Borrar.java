    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        boolean borrado;
        String ci;
        ci = txtCI.getText().trim();
        if (ci.equals("")) {
            JOptionPane.showMessageDialog(this, "No se ingreso una c√©dula", "CRUD ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                borrado = Sistema.getInstance().borrarUsuario(ci);
                if (borrado) {
                    JOptionPane.showMessageDialog(this, "Se borro el usuario correctamente", "CRUD System", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "La cedula del usuario no se encuentra en el sistema", "CRUD ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "CRUD System", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

