    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombre;
        String apellido;
        String ci;
        nombre = txtNombre.getText().trim();
        apellido = txtApellido.getText().trim();
        ci = txtCI.getText().trim();
        if (nombre.equals("") || apellido.equals("") || ci.equals("")) {
            JOptionPane.showMessageDialog(this, "No se puede agregar el usuario a la bd", "CRUD ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else {
            try {
                Usuario u = new Usuario(nombre, apellido, ci);
                Sistema.getInstance().agregarUsuario(u);
                JOptionPane.showMessageDialog(this, "Se ha agregado al empleado con Ã©xito", "CRUD System", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "CRUD System", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

